# ตัวดำเนินการทางคณิตศาสตร์

# ตัวดำเนินการ & ตัวที่ถูกดำเนินการ
# Operator | Operand

# A + B
# A , B => Operand
# +,... => Operator

x = 5
y = 10 

print("ผลบวก", x+y)
print("ผลลบ", x-y)
print("ผลคูณ", x*y)
print("ผลหาร", x/y)
print("ผลหารปัดเศษ", x//y)
print("ผลยกกำลัง", 5**2)
print("ผลหารเอาเศษ", x%y)